/*
 * Reads the calibrator's JSON output and regenerates the catalogs that
 * the game's shared state imports:
 *
 *   tools/cosmetics/cosmetics.json      →  src/shared/cosmetics-catalog.generated.ts
 *   tools/cats/cats.json                →  src/shared/cats-catalog.generated.ts
 *   tools/themes/themes.json            →  src/shared/themes-catalog.generated.ts
 *   tools/music/music.json              →  src/shared/music-catalog.generated.ts
 *
 * The tools server runs this after every successful POST /save so the
 * game's catalog stays in lock-step with the calibrators without any
 * manual editing. Also runnable directly: `npm run sync:catalog`.
 */
import fs from 'node:fs/promises';
import path from 'node:path';

const PROJECT_ROOT = path.resolve(import.meta.dirname, '..');
const COSMETICS_JSON = path.join(PROJECT_ROOT, 'tools', 'cosmetics', 'cosmetics.json');
const CATS_JSON = path.join(PROJECT_ROOT, 'tools', 'cats', 'cats.json');
// TODO Phase 5: DECORATIONS_JSON removed with decoration system
const THEMES_JSON = path.join(PROJECT_ROOT, 'tools', 'themes', 'themes.json');
const MUSIC_JSON = path.join(PROJECT_ROOT, 'tools', 'music', 'music.json');
const OUT_DIR = path.join(PROJECT_ROOT, 'src', 'shared');

interface CosmeticJsonEntry {
  id: string;
  name: string;
  rarity: string;
  slot?: string;
  scale?: number;
  offsetX?: number;
  offsetY?: number;
  sourceFrame?: string;
  tint?: string;
  tintMode?: string;
  /** When true, this cosmetic has no per-frame animation art and the
   *  game's Cat entity rides it through the cat's per-frame translation
   *  offsets (read from cat-frame-offsets.json) so the cosmetic bobs /
   *  jumps with its cat. Set automatically by the Cosmetic Quick Add
   *  upload flow for single-PNG cosmetics. Defaults to false — existing
   *  hand-animated cosmetics keep their per-frame art driving motion. */
  isStatic?: boolean;
  /** Optional per-cosmetic override of the slot-default motion strength.
   *  1.0 = tracks the cat 1:1, 0 = doesn't ride motion at all. Only
   *  used when `isStatic: true`. */
  motionStrength?: number;
}

interface CatJsonEntry {
  id: string;
  name: string;
  rarity: string;
  scale?: number;
  sourceFrame?: string;
  tint?: string;
  tintMode?: string;
}

// TODO Phase 5: DecorationJsonEntry removed with decoration system

interface ThemeJsonEntry {
  displayName: string;
  backdropKey: string;
  musicKey: string;
  rarity: string;
  // Per-bg vertical shift in DESIGN pixels — positive shifts the bg image
  // UP so platforms in the source PNG align with the cat seats at design
  // y=184. Calibrated via tools/themes/calibrator.html. Default 0.
  bgShiftUp?: number;
  // Per-bg uniform scale multiplier — > 1 crops the edges (zoom in on
  // the platforms), < 1 leaves empty space around the edges. Default 1.
  bgScale?: number;
}

const BANNER =
  '// AUTO-GENERATED by scripts/sync-catalog.ts.\n' +
  '// Do not edit by hand — your changes will be overwritten.\n' +
  '// Edit tools/<tool>/*.json via the calibrator instead.\n';

function fieldLines(fields: Record<string, unknown>): string[] {
  const out: string[] = [];
  for (const [k, v] of Object.entries(fields)) {
    if (v === undefined || v === null) continue;
    out.push(`    ${k}: ${JSON.stringify(v)},`);
  }
  return out;
}

async function readJsonOrEmpty<T>(file: string): Promise<T[]> {
  try {
    const text = await fs.readFile(file, 'utf8');
    const parsed = JSON.parse(text);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

async function genCosmetics(): Promise<number> {
  const arr = await readJsonOrEmpty<CosmeticJsonEntry>(COSMETICS_JSON);
  const lines: string[] = [
    BANNER,
    `import type { CosmeticEntry } from './state';`,
    '',
    `export const GENERATED_COSMETIC_CATALOG: readonly CosmeticEntry[] = [`,
  ];
  for (const e of arr) {
    lines.push('  {');
    lines.push(
      ...fieldLines({
        id: e.id,
        name: e.name,
        rarity: e.rarity,
        slot: e.slot,
        sourceFrame: e.sourceFrame,
        tint: e.tint,
        tintMode: e.tintMode,
        isStatic: e.isStatic,
        motionStrength: e.motionStrength,
      }),
    );
    lines.push('  },');
  }
  lines.push('];');
  lines.push('');
  await fs.writeFile(
    path.join(OUT_DIR, 'cosmetics-catalog.generated.ts'),
    lines.join('\n'),
  );
  return arr.length;
}

async function genCats(): Promise<number> {
  const arr = await readJsonOrEmpty<CatJsonEntry>(CATS_JSON);
  // If cats.json doesn't exist yet, ship the 6 base cats as defaults so the
  // game has something to work with until the cat calibrator gets used.
  const entries: CatJsonEntry[] = arr.length > 0 ? arr : [
    { id: 'cat1', name: 'Mochi', rarity: 'common' },
    { id: 'cat2', name: 'Biscuit', rarity: 'common' },
    { id: 'cat3', name: 'Pebble', rarity: 'common' },
    { id: 'cat4', name: 'Marble', rarity: 'uncommon' },
    { id: 'cat5', name: 'Saffron', rarity: 'rare' },
    { id: 'cat6', name: 'Inkwell', rarity: 'rare' },
    { id: 'rainbow', name: 'Rainbow Whiskers', rarity: 'legendary' },
  ];
  const lines: string[] = [
    BANNER,
    `import type { CatEntry } from './state';`,
    '',
    `export const GENERATED_CAT_CATALOG: readonly CatEntry[] = [`,
  ];
  for (const e of entries) {
    lines.push('  {');
    lines.push(
      ...fieldLines({
        id: e.id,
        name: e.name,
        rarity: e.rarity,
        scale: e.scale,
        sourceFrame: e.sourceFrame,
        tint: e.tint,
        tintMode: e.tintMode,
      }),
    );
    lines.push('  },');
  }
  lines.push('];');
  lines.push('');
  await fs.writeFile(
    path.join(OUT_DIR, 'cats-catalog.generated.ts'),
    lines.join('\n'),
  );
  return entries.length;
}

