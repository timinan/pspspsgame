async function genThemes(): Promise<number> {
  let raw: Record<string, ThemeJsonEntry> = {};
  try {
    const text = await fs.readFile(THEMES_JSON, 'utf8');
    raw = JSON.parse(text) as Record<string, ThemeJsonEntry>;
  } catch {
    // file not found — write an empty catalog
  }
  const entries = Object.entries(raw);
  const catalogLines: string[] = [];
  for (const [id, v] of entries) {
    const optional: string[] = [];
    if (typeof v.bgShiftUp === 'number') optional.push(`bgShiftUp: ${v.bgShiftUp}`);
    if (typeof v.bgScale === 'number') optional.push(`bgScale: ${v.bgScale}`);
    const optionalStr = optional.length > 0 ? `, ${optional.join(', ')}` : '';
    // JSON.stringify the key so hyphenated ids ("hidden-oasis-cave")
    // emit as ["hidden-oasis-cave"]: instead of an unquoted hidden-oasis-cave:
    // which is a vite/rolldown parse error. Same below for the array-items
    // access form.
    catalogLines.push(
      `  ${JSON.stringify(id)}: { id: ${JSON.stringify(id)}, displayName: ${JSON.stringify(v.displayName)}, backdropKey: ${JSON.stringify(v.backdropKey)}, musicKey: ${JSON.stringify(v.musicKey)}, rarity: ${JSON.stringify(v.rarity)} as const${optionalStr} },`,
    );
  }
  const arrayItems = entries.map(([id]) => `  BACKGROUND_CATALOG[${JSON.stringify(id)}],`);
  const lines: string[] = [
    BANNER,
    '',
    `export const BACKGROUND_CATALOG = {`,
    ...catalogLines,
    `} as const;`,
    '',
    `// Legacy alias — keep so existing THEME_CATALOG consumers compile without a rename sweep.`,
    `// TODO Phase 5 Task 15: remove this alias once all consumers migrate to BACKGROUND_CATALOG.`,
    `export const GENERATED_THEME_CATALOG = [`,
    ...arrayItems,
    `] as const;`,
    '',
  ];
  await fs.writeFile(
    path.join(OUT_DIR, 'themes-catalog.generated.ts'),
    lines.join('\n'),
  );
  return entries.length;
}

interface MusicJsonEntry {
  id: string;
  displayName?: string;
  speedLabel: 'slow' | 'medium' | 'fast' | 'faster';
  vibe: 'upbeat' | 'melodic' | 'smooth';
  /** Musical genre slug — must match BackingGenre union in state.ts.
   *  Optional so old entries pre-genre-system still validate. */
  genre?: string;
  /** Emotional mood slug — must match BackingMood union in state.ts. */
  mood?: string;
  bpm: number;
  loopDurationMs?: number;
  /** Auto-detected (or Tim-overridden) start time of the clip inside
   *  the source mp3. Optional — old entries pre-section-finder lack it. */
  clipStartS?: number;
}

async function genMusic(): Promise<number> {
  let raw: Record<string, MusicJsonEntry> = {};
  try {
    const text = await fs.readFile(MUSIC_JSON, 'utf8');
    raw = JSON.parse(text) as Record<string, MusicJsonEntry>;
  } catch {
    // missing or unparseable — write an empty catalog so MusicSystem stays silent
  }
  // Read user-extensible taxonomies (vibes/genres/moods) so the game's
  // SongPicker can offer any custom values Tim added via the
  // calibrator. Falls back to the canonical seed if the file's missing.
  let tax: { vibes?: string[]; genres?: string[]; moods?: string[] } = {};
  try {
    const taxText = await fs.readFile(
      path.join(PROJECT_ROOT, 'tools', 'music', 'taxonomies.json'),
      'utf8',
    );
    tax = JSON.parse(taxText) as typeof tax;
  } catch {
    // missing — use defaults below
  }
  const vibes = tax.vibes ?? ['upbeat', 'melodic', 'smooth'];
  const genres = tax.genres ?? [
    'bubblegum-pop', 'synthwave', 'lo-fi', 'bossa-nova', 'acoustic-folk',
    'chiptune', 'jazz', 'funk', 'ambient', 'house',
    'rock', 'dance-pop', 'trance', 'hip-hop', 'world',
  ];
  const moods = tax.moods ?? [
    'energetic', 'chill', 'playful', 'romantic', 'mysterious',
    'dramatic', 'nostalgic', 'bright', 'dark', 'dreamy',
  ];
  const entries = Object.entries(raw);
  const catalogLines: string[] = [];
  for (const [id, v] of entries) {
    const loopDur = typeof v.loopDurationMs === 'number' ? v.loopDurationMs : 30_000;
    const genrePart = v.genre ? `, genre: ${JSON.stringify(v.genre)}` : '';
    const moodPart = v.mood ? `, mood: ${JSON.stringify(v.mood)}` : '';
    const clipStartPart = typeof v.clipStartS === 'number' ? `, clipStartS: ${v.clipStartS}` : '';
    catalogLines.push(
      `  ${JSON.stringify(id)}: { id: ${JSON.stringify(id)}, displayName: ${JSON.stringify(v.displayName ?? id)}, speedLabel: ${JSON.stringify(v.speedLabel)}, vibe: ${JSON.stringify(v.vibe)}${genrePart}${moodPart}, bpm: ${v.bpm}, audioKey: ${JSON.stringify('backing-' + id)}, loopDurationMs: ${loopDur}${clipStartPart} },`,
    );
  }
  const lines: string[] = [
    BANNER,
    '',
    `import type { BackingTrack } from './state';`,
    '',
    `// User-extensible taxonomies sourced from tools/music/taxonomies.json.`,
    `// SongPicker + calibrator read these to populate genre/mood/vibe pickers.`,
    `export const BACKING_VIBES: readonly string[] = ${JSON.stringify(vibes)};`,
    `export const BACKING_GENRES: readonly string[] = ${JSON.stringify(genres)};`,
    `export const BACKING_MOODS: readonly string[] = ${JSON.stringify(moods)};`,
    '',
    `export const BACKING_CATALOG: Record<string, BackingTrack> = {`,
    ...catalogLines,
    `};`,
    '',
  ];
  await fs.writeFile(
    path.join(OUT_DIR, 'music-catalog.generated.ts'),
    lines.join('\n'),
  );
  return entries.length;
}

async function main(): Promise<void> {
  await fs.mkdir(OUT_DIR, { recursive: true });
  const [cosCount, catCount, themCount, musicCount] = await Promise.all([
    genCosmetics(),
    genCats(),
    genThemes(),
    genMusic(),
  ]);
  console.log(`[sync] cosmetics=${cosCount} cats=${catCount} themes=${themCount} music=${musicCount}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
